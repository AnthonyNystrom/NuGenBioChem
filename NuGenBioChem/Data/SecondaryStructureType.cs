
namespace NuGenBioChem.Data
{
    /// <summary>
    /// Types of the protein's secondary structure
    /// </summary>
    public enum SecondaryStructureType
    {
        /// <summary>
        /// Empty secondary structure type
        /// </summary>
        NotDefined,

        /// <summary>
        /// Alfa-Helix structure (right-handed coil)
        /// </summary>
        Helix,

        /// <summary>
        /// Betta-Sheet structure (connected betta-strands)
        /// </summary>
        Sheet
    }
}
